%--------------------------------------------------------------------------
%********************calculation of the Data error*************************
%--------------------------------------------------------------------------

%--------------------------------------------------------------------------
%Inputs:
%   N=the lengh of the data
%   y_noisy=noisy output
%   y_estimated=the estimated ouput
%--------------------------------------------------------------------------



function output = xsm( N,y_noisy,y_estimated )

x1=(y_noisy)-(y_estimated);
x2=norm(x1,2);
x3=x2^2;
output=(1/N)*x3;

end

